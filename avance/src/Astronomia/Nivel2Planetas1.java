    private void activarClickEnPlanetas() {
        for (PlanetaNivel2 planeta : planetas) {
            planeta.label.setCursor(new Cursor(Cursor.HAND_CURSOR));
            planeta.label.addMouseListener(new java.awt.event.MouseAdapter() {
                @Override
                public void mouseClicked(java.awt.event.MouseEvent evt) {
                    verificarPlaneta(planeta);
                }
            });
        }
    }

    private void iniciarMovimientoPlanetas() {
        timerMovimiento = new Timer(30, e -> {
            for (PlanetaNivel2 planeta : planetas) {
                planeta.mover();
            }
        });
        timerMovimiento.start();
    }

    private void verificarPlaneta(PlanetaNivel2 planeta) {
        MisionNivel2 mision = misiones.get(numeroMision);

        if (mision.esCorrecto(planeta.nombre)) {
            if (planetasSeleccionados.contains(planeta.nombre)) {
                lblMensajeJuego.setText("Ese planeta ya fue seleccionado.");
                return;
            }

            planetasSeleccionados.add(planeta.nombre);
            planetasPendientes.remove(planeta.nombre);
            planeta.label.setBorder(new LineBorder(Color.YELLOW, 3));
            explosionEstrellas(planeta.label);

            if (planetasPendientes.isEmpty()) {
                GameData.sumarPuntos();
                lblMensajeJuego.setText("Muy bien: +10 puntos");
                pasarASiguienteMision();
            } else {
                lblMensajeJuego.setText("Bien. Sigue buscando los planetas correctos.");
            }
        } else {
            GameData.restarPuntos();
            lblMensajeJuego.setText("Incorrecto: -3 puntos");
        }

        actualizarPuntaje();
    }

    private void pasarASiguienteMision() {
        numeroMision++;

        if (numeroMision >= misiones.size()) {
            numeroMision = 0;
            JOptionPane.showMessageDialog(this, "Completaste todas las misiones del Nivel 2.");
        }

        mostrarMisionActual();
    }

    private void mostrarMisionActual() {
        quitarBordesDePlanetas();

        MisionNivel2 mision = misiones.get(numeroMision);
        planetasPendientes.clear();
        planetasSeleccionados.clear();
        planetasPendientes.addAll(mision.respuestasCorrectas);

        lblPregunta.setText("<html><center>" + mision.pregunta + "</center></html>");
        actualizarPuntaje();
    }

    private void quitarBordesDePlanetas() {
        for (PlanetaNivel2 planeta : planetas) {
            planeta.label.setBorder(null);
        }
    }

    private void actualizarPuntaje() {
        lblPuntaje.setText("Puntos: " + GameData.puntos);
    }

    private void usarPista() {
        if (GameData.puntos <= 0) {
            lblMensajeJuego.setText("Gastaste todos tus puntos");
            actualizarPuntaje();
            return;
        }
        GameData.restarPuntos();
        lblMensajeJuego.setText(misiones.get(numeroMision).pista);
        actualizarPuntaje();
    }

