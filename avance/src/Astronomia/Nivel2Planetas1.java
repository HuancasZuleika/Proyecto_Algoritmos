    private void explosionEstrellas(JLabel planeta) {
        int centroX = planeta.getX() + planeta.getWidth() / 2;
        int centroY = planeta.getY() + planeta.getHeight() / 2;

        for (int i = 0; i < 8; i++) {
            JLabel estrella = new JLabel("*");
            estrella.setFont(new Font("Arial", Font.BOLD, 28));
            estrella.setForeground(Color.YELLOW);

            int x = centroX - 10 + random.nextInt(60) - 30;
            int y = centroY - 10 + random.nextInt(60) - 30;
            getContentPane().add(estrella, new org.netbeans.lib.awtextra.AbsoluteConstraints(x, y, 30, 30));
            getContentPane().setComponentZOrder(estrella, 0);

            Timer borrar = new Timer(600, e -> {
                getContentPane().remove(estrella);
                getContentPane().repaint();
            });
            borrar.setRepeats(false);
            borrar.start();
        }

        getContentPane().repaint();
    }

    private class PlanetaNivel2 {

        String nombre;
        String tipo;
        JLabel label;
        int dx;
        int dy;

        PlanetaNivel2(String nombre, String tipo, JLabel label, int dx, int dy) {
            this.nombre = nombre;
            this.tipo = tipo;
            this.label = label;
            this.dx = dx;
            this.dy = dy;
        }

        void mover() {
            int nuevoX = label.getX() + dx;
            int nuevoY = label.getY() + dy;

            int limiteIzquierdo = 20;
            int limiteDerecho = getContentPane().getWidth() - label.getWidth() - 20;
            int limiteArriba = 145;
            int limiteAbajo = getContentPane().getHeight() - label.getHeight() - 85;

            if (nuevoX <= limiteIzquierdo || nuevoX >= limiteDerecho) {
                dx = dx * -1;
            }

            if (nuevoY <= limiteArriba || nuevoY >= limiteAbajo) {
                dy = dy * -1;
            }

            // Zona prohibida: caja donde esta el texto de MISION.
            // Ajusta estos numeros si tu panel de mision esta en otra posicion.
            Rectangle zonaMision = new Rectangle(45, 45, 400, 200);
            Rectangle futuroPlaneta = new Rectangle(nuevoX, nuevoY, label.getWidth(), label.getHeight());

            if (futuroPlaneta.intersects(zonaMision)) {
                dx = dx * -1;
                dy = dy * -1;
                nuevoX = label.getX() + dx;
                nuevoY = label.getY() + dy;
            }

            label.setLocation(label.getX() + dx, label.getY() + dy);
        }
    }

    private static class MisionNivel2 {

        String pregunta;
        String pista;
        Set<String> respuestasCorrectas;

        MisionNivel2(String pregunta, String pista, String... respuestas) {
            this.pregunta = pregunta;
            this.pista = pista;
            this.respuestasCorrectas = new HashSet<>(Arrays.asList(respuestas));
        }

        boolean esCorrecto(String nombrePlaneta) {
            return respuestasCorrectas.contains(nombrePlaneta);
        }
    }


