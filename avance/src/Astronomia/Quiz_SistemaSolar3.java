    private void animarBoton(javax.swing.JButton boton) {
        java.awt.Color colorOriginal = boton.getBackground();
        // Cambiamos a un tono de azul suave
        boton.setBackground(new java.awt.Color(173, 216, 230));
        boton.setOpaque(true);
        boton.setContentAreaFilled(true); // Necesario para que el color sea visible

        javax.swing.Timer timer = new javax.swing.Timer(300, e -> {
            boton.setBackground(colorOriginal);
            boton.setContentAreaFilled(false); // Regresa a tu estilo transparente
            ((javax.swing.Timer) e.getSource()).stop();
        });
        timer.setRepeats(false);
        timer.start();
    }

