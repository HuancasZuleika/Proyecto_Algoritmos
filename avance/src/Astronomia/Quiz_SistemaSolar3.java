  private void procesarRespuesta(boolean esCorrecto, javax.swing.JButton boton) {
        // Ejecutamos la animación de vibración
        animarBoton(boton);

        if (esCorrecto) {
            respuestaCorrecta = true;
            javax.swing.JOptionPane.showMessageDialog(this, "¡Correcto!");
        } else {
            respuestaCorrecta = false;
            // Solo restamos vida si es incorrecto
            GameData.restarVida();

            if (GameData.vidas > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Incorrecto. Te quedan " + GameData.vidas + " vidas.");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "¡Incorrecto! Has perdido todas tus vidas. Game Over.");
                System.exit(0);
            }
        }
    }


