    private void configurarAnimaciones() {
        javax.swing.JButton[] botones = {jbtA, jbtB, jbtC};
        for (javax.swing.JButton b : botones) {
            b.addMouseListener(new java.awt.event.MouseAdapter() {
                public void mouseEntered(java.awt.event.MouseEvent evt) { animarZoom(b, true); }
                public void mouseExited(java.awt.event.MouseEvent evt) { animarZoom(b, false); }
            });
        }
    }

