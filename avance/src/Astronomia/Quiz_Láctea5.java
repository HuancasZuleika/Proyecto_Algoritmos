    private void animarZoom(javax.swing.JButton boton, boolean entrar) {
        java.awt.Dimension original = new java.awt.Dimension(50, 50); // Tamaño base
        if (entrar) {
            boton.setSize(60, 60); // Aumenta tamaño
        } else {
            boton.setSize(original); // Vuelve al original
        }
    }
    
