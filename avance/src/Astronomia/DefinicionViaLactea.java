    private void activarBigBangEnBoton(JButton boton) {
        if (boton.getIcon() == null) return;

        boton.setOpaque(false);
        boton.setContentAreaFilled(false);
        boton.setBorderPainted(false);
        boton.setFocusPainted(false);
        boton.setBackground(new java.awt.Color(0, 0, 0, 0));

        final boolean[] mouseEncima = {false};
        final float[] progresoEstrella = {0.0f};

        final java.util.List<Point> puntosContorno = new ArrayList<>();
        final java.util.List<Double> angulosContorno = new ArrayList<>();
        final java.util.List<ParticulaCosmica> listaParticulas = new ArrayList<>();
        final Random random = new Random();

        final Timer timerAnimacion = new Timer(16, e -> {
            progresoEstrella[0] += 0.012f;
            if (progresoEstrella[0] > 1.0f) progresoEstrella[0] = 0.0f;

            if (mouseEncima[0] && !puntosContorno.isEmpty()) {
                int totalPuntos = puntosContorno.size();

                // Flujo constante de chispas de luz
                for (int k = 0; k < 5; k++) { 
                    int indiceAleatorio = random.nextInt(totalPuntos);
                    Point p = puntosContorno.get(indiceAleatorio);
                    double anguloDisparo = angulosContorno.get(indiceAleatorio);

                    anguloDisparo += (random.nextDouble() - 0.5) * 0.8;
                    listaParticulas.add(new ParticulaCosmica(p.x, p.y, anguloDisparo, random));
                }
            }

            Iterator<ParticulaCosmica> iter = listaParticulas.iterator();
            while (iter.hasNext()) {
                ParticulaCosmica p = iter.next();
                p.actualizar();
                if (p.opacidad <= 0) {
                    iter.remove();
                }
            }

            boton.repaint();
        });

        boton.setUI(new BasicButtonUI() {
            @Override
            public void paint(Graphics g, javax.swing.JComponent c) {
                Graphics2D g2d = (Graphics2D) g.create();

                g2d.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
                g2d.setRenderingHint(RenderingHints.KEY_ALPHA_INTERPOLATION, RenderingHints.VALUE_ALPHA_INTERPOLATION_QUALITY);

                g2d.setComposite(AlphaComposite.Clear);
                g2d.fillRect(0, 0, c.getWidth(), c.getHeight());
                g2d.setComposite(AlphaComposite.SrcOver);

                // --- 1. RENDERIZAR PARTÍCULAS CON ILUMINACIÓN REAL ---
                for (ParticulaCosmica p : listaParticulas) {
                    p.dibujar(g2d); // Llama al nuevo método de doble capa de color
                }
                g2d.setComposite(AlphaComposite.SrcOver); 

                // --- 2. RENDERIZAR LA ESTRELLA GUÍA ORBITAL ---
                if (mouseEncima[0] && !puntosContorno.isEmpty()) {
                    int totalPuntos = puntosContorno.size();
                    int indiceActual = (int) (progresoEstrella[0] * (totalPuntos - 1));
                    Point puntoEstrella = puntosContorno.get(indiceActual);

                    // Corrección de NetBeans: Guardamos la transformación de forma nativa
                    java.awt.geom.AffineTransform oldTransform = g2d.getTransform();

                    g2d.translate(puntoEstrella.x, puntoEstrella.y);
                    g2d.rotate(progresoEstrella[0] * Math.PI * 8); 
                    g2d.setColor(new Color(255, 240, 150)); 

                    java.awt.geom.Path2D estrellaPath = new java.awt.geom.Path2D.Double();
                    double rExter = 7.0; 
                    double rInter = 3.0; 
                    estrellaPath.moveTo(0, -rExter);
                    for (int i = 1; i < 10; i++) {
                        double r = (i % 2 == 0) ? rExter : rInter;
                        double angulo = i * Math.PI / 5.0;
                        estrellaPath.lineTo(r * Math.sin(angulo), -r * Math.cos(angulo));
                    }
                    estrellaPath.closePath();
                    g2d.fill(estrellaPath);

                    g2d.setTransform(oldTransform); // Corrección de NetBeans: Restauramos
                }

                g2d.dispose();
                super.paint(g, c); 
            }
        });

        boton.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseEntered(MouseEvent e) {
                mouseEncima[0] = true;

                if (puntosContorno.isEmpty()) {
                    int centroX = boton.getWidth() / 2;
                    int centroY = boton.getHeight() / 2;

                    // Radio circular exacto para rodear tu explosión de forma perfecta
                    int radioExplosion = 15; 

                    for (int i = 0; i < 360; i++) {
                        double anguloRad = Math.toRadians(i);
                        int x = centroX + (int) (Math.cos(anguloRad) * radioExplosion);
                        int y = centroY + (int) (Math.sin(anguloRad) * radioExplosion);

                        puntosContorno.add(new Point(x, y));
                        angulosContorno.add(anguloRad);
                    }
                }

                // ¡EL IMPACTO DEL BIG BANG INICIAL CON ILUMINACIÓN!
                if (!puntosContorno.isEmpty()) {
                    for (int i = 0; i < 30; i++) { 
                        int randIdx = random.nextInt(puntosContorno.size());
                        Point p = puntosContorno.get(randIdx);
                        double ang = angulosContorno.get(randIdx) + (random.nextDouble() - 0.5) * 0.8;
                        listaParticulas.add(new ParticulaCosmica(p.x, p.y, ang, random));
                    }
                }

                timerAnimacion.start();
            }

            @Override
            public void mouseExited(MouseEvent e) {
                mouseEncima[0] = false;
                Timer retrasoApagado = new Timer(800, ev -> {
                    if (!mouseEncima[0] && listaParticulas.isEmpty()) {
                        timerAnimacion.stop();
                    }
                });
                retrasoApagado.setRepeats(false);
                retrasoApagado.start();
            }
        });
    }
    
