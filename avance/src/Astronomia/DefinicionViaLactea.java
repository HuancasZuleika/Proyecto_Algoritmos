    private void EfectoBoton(javax.swing.JButton boton) {
            if (boton.getIcon() == null) return;

            ImageIcon imgOriginal = (ImageIcon) boton.getIcon();
            int anchoNormal = imgOriginal.getIconWidth();
            int altoNormal = imgOriginal.getIconHeight();

            // Calculamos las tres escalas:
            int anchoHover = (int) (anchoNormal * 1.02); // Un poco más grande (Hover)
            int altoHover = (int) (altoNormal * 1.02);

            int anchoClick = (int) (anchoNormal * 0.90); // Un poco más chico (Efecto hundido/presionado)
            int altoClick = (int) (altoNormal * 0.90);

            // Creamos los tres iconos escalados
            javax.swing.Icon iconoNormal = new ImageIcon(imgOriginal.getImage().getScaledInstance(anchoNormal, altoNormal, java.awt.Image.SCALE_DEFAULT));
            javax.swing.Icon iconoHover = new ImageIcon(imgOriginal.getImage().getScaledInstance(anchoHover, altoHover, java.awt.Image.SCALE_DEFAULT));
            javax.swing.Icon iconoClick = new ImageIcon(imgOriginal.getImage().getScaledInstance(anchoClick, altoClick, java.awt.Image.SCALE_DEFAULT));

            // Estética del botón
            boton.setContentAreaFilled(false);
            boton.setBorderPainted(false);
            boton.setFocusPainted(false);
            boton.setIcon(iconoNormal);

            // Control total de la ilusión de presión
            boton.addMouseListener(new java.awt.event.MouseAdapter() {
                @Override
                public void mouseEntered(java.awt.event.MouseEvent e) {
                    boton.setIcon(iconoHover); // Crece al pasar el mouse
                }

                @Override
                public void mouseExited(java.awt.event.MouseEvent e) {
                    boton.setIcon(iconoNormal); // Regresa al tamaño base al salir
                }

                @Override
                public void mousePressed(java.awt.event.MouseEvent e) {
                    boton.setIcon(iconoClick); // ¡Se hunde al hacer click!
                }

                @Override
                public void mouseReleased(java.awt.event.MouseEvent e) {
                    // Si sueltas el click dentro del botón, vuelve al tamaño hover
                    if (boton.getBounds().contains(e.getPoint())) {
                        boton.setIcon(iconoHover);
                    } else {
                        boton.setIcon(iconoNormal);
                    }
                }
            });
    }
    
    private static class ParticulaCosmica {
        double x, y;
        double velX, velY;
        float opacidad = 1.0f;
        double tamano;
        Color colorResplandor;
        Color colorCentro;
        double friccion = 1; 

        ParticulaCosmica(double x, double y, double angulo, java.util.Random random) {
            this.x = x;
            this.y = y;

            // Velocidad de la explosión cósmica
            double velocidad = 3.0 + random.nextDouble() * 6.0; // Cambiamos el (3.0 y 6.0) por más potencia
            this.velX = Math.cos(angulo) * velocidad;
            this.velY = Math.sin(angulo) * velocidad;

            // Tamaño de los destellos luminosos
            this.tamano = 3.0 + random.nextDouble() * 5.0; 

            // CONFIGURACIÓN DE ILUMINACIÓN DE TU IMAGEN (Núcleo brillante + Resplandor de luz)
            int probabilidad = random.nextInt(100);
            if (probabilidad < 75) {
                // Estrellas doradas hiper-iluminadas (Como tu guirnalda)
                this.colorResplandor = new Color(255, 170, 0, 180); // Aura naranja/oro traslúcida
                this.colorCentro = new Color(255, 255, 200);        // Centro amarillo/blanco brillante
            } else {
                // Destellos de energía pura blanca
                this.colorResplandor = new Color(255, 230, 130, 150); // Aura amarilla suave
                this.colorCentro = Color.WHITE;                       // Núcleo blanco incandescente
            }
        }

        void actualizar() {
            velX *= friccion;
            velY *= friccion;
            x += velX;
            y += velY;

            opacidad -= 0.025f; 
            if (opacidad < 0) opacidad = 0;
        }

        // Dibuja el efecto de iluminación real (Glow Effect)
        void dibujar(Graphics2D g2d) {
            // Ajustamos la opacidad general de la partícula según su tiempo de vida
            g2d.setComposite(AlphaComposite.getInstance(AlphaComposite.SRC_OVER, opacidad));

            // 1. DIBUJAR EL RESPLANDOR (Círculo difuminado externo más grande)
            g2d.setColor(colorResplandor);
            double tamanoGlow = tamano * 2.2; // El aura es más del doble de grande
            g2d.fill(new java.awt.geom.Ellipse2D.Double(x - tamanoGlow/2.0, y - tamanoGlow/2.0, tamanoGlow, tamanoGlow));

            // 2. DIBUJAR EL NÚCLEO ENCIENDIDO (Círculo central brillante)
            g2d.setColor(colorCentro);
            g2d.fill(new java.awt.geom.Ellipse2D.Double(x - tamano/2.0, y - tamano/2.0, tamano, tamano));
        }
    }

