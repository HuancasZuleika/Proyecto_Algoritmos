    private void btnPistaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPistaActionPerformed
        // TODO add your handling code here:

        if (respuestaCorrecta) {
            return;
        }

        if (!pistaUsada) {
            Sonido.reproducir("idea.wav");
            GameData.restarPuntos();
            actualizarPuntos();
            jLabel3.setIcon(new ImageIcon(getClass().getResource("/imagen/astro_sorpresa.png")));

            //texto para la pista
            setPistaTexto("Los científicos observaron que las galaxias se alejan unas de otras.");

            // Muestra tu panel personalizado con la imagen idea_pista.png
            panelPista.setVisible(true);

            pistaUsada = true;
            btnPista.setEnabled(false);

        } else {
            JOptionPane.showMessageDialog(
                    this,
                    "Ya utilizaste la pista."
            );
        }
    }//GEN-LAST:event_btnPistaActionPerformed

