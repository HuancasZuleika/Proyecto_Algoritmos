    private void crearPanelMensajePersonalizado() {
        panelMensaje = new javax.swing.JPanel();
        panelMensaje.setLayout(null);
        panelMensaje.setOpaque(false);

        // 🟢 1. Tamaño total del área emergente (más alto para albergar el botón abajo)
        int ancho = 520;
        int alto = 220;

        // Posicionamos el panel completo un poco más arriba para centrarlo (Y: 210)
        panelMensaje.setBounds(150, 245, ancho, alto);
        panelMensaje.setVisible(false);

        // 🟢 2. Fondo semitransparente (Oscurece lo que está detrás para dar efecto emergente)
        javax.swing.JPanel fondoOscuro = new javax.swing.JPanel();
        fondoOscuro.setBackground(new java.awt.Color(0, 0, 0, 190)); // Negro con transparencia (alpha 150)
        fondoOscuro.setBounds(0, 0, ancho, alto);
        // Para bordes redondeados bonitos detrás del cartel
        fondoOscuro.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 255, 255, 80), 2, true));

        // 🟢 3. Botón "CONTINUAR" dentro del área visible
        javax.swing.JButton btnContinuar = new javax.swing.JButton("¡CONTINUAR!");
        btnContinuar.setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 14));
        btnContinuar.setForeground(java.awt.Color.WHITE);
        btnContinuar.setBackground(new java.awt.Color(40, 167, 69)); // Verde limpio brillante
        btnContinuar.setFocusPainted(false);
        btnContinuar.setBorder(javax.swing.BorderFactory.createEtchedBorder());
        btnContinuar.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));

        // Y = 165 ubica el botón justo debajo de la imagen verde sin cortarse
        btnContinuar.setBounds(175, 165, 170, 38);

        btnContinuar.addActionListener(e -> {
            panelMensaje.setVisible(false);
            muñeco.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagen/muñequitolike.png")));
            muñeco.repaint();
        });

        // 🟢 4. Imagen verde del mensaje (alto: 150)
        javax.swing.JLabel fondoVerde = new javax.swing.JLabel();
        java.net.URL url = getClass().getResource("/imagen/rpta. correcta.png");

        if (url != null) {
            ImageIcon icon = new ImageIcon(url);
            java.awt.Image img = icon.getImage().getScaledInstance(ancho, 150, java.awt.Image.SCALE_SMOOTH);
            fondoVerde.setIcon(new ImageIcon(img));
        }
        fondoVerde.setBounds(0, 0, ancho, 150);

        // 🟢 5. Agregar componentes en ORDEN DE CAPAS (Arriba -> Abajo)
        panelMensaje.add(btnContinuar);  // Capa superior: Botón
        panelMensaje.add(fondoVerde);    // Capa media: Imagen verde
        panelMensaje.add(fondoOscuro);   // Capa inferior: Sombra oscura

        // 🟢 6. Agregar al contenedor principal
        getContentPane().add(panelMensaje,
                new org.netbeans.lib.awtextra.AbsoluteConstraints(150, 245, ancho, alto));

        getContentPane().setComponentZOrder(panelMensaje, 0);
    }

