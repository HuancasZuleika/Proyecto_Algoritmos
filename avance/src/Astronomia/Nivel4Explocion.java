    private void registrarBasura(JLabel basura) {
        objetosBasura.add(basura);
        basuraTotal++;
    }

    private void crearBasura(int x, int y) {
        final JLabel basura = new JLabel();
        basura.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagen/satelite-removebg-preview (1).png")));
        basura.setCursor(new Cursor(Cursor.HAND_CURSOR));
        basura.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent evt) {
                eliminarBasura(basura);
            }
        });

        registrarBasura(basura);
        agregarComponente(basura, x, y, 70, 60);
        getContentPane().setComponentZOrder(basura, 0);
    }

    private void crearEstrella(int x, int y) {
        final JLabel estrella = new JLabel();
        estrella.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagen/estrella-removebg-preview (1).png")));
        estrella.setCursor(new Cursor(Cursor.HAND_CURSOR));
        estrella.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent evt) {
                tocarEstrella(estrella);
            }
        });

        agregarComponente(estrella, x, y, 80, 70);
        getContentPane().setComponentZOrder(estrella, 0);
    }

    private void eliminarBasura(JLabel basura) {
        if (!basura.isVisible()) {
            return;
        }

        basura.setVisible(false);
        basuraEliminada++;

        if (basuraEliminada % 4 == 0) {
            puntos += 10;
            lblMensaje.setText("Muy bien: limpiaste 4 basuras. +10 puntos.");
            mostrarVentanaBonita("Buen trabajo", "Eliminaste 4 objetos de basura espacial.\nGanaste 10 puntos.\nPuntaje actual: " + puntos);
        } else {
            lblMensaje.setText("Basura eliminada. Sigue limpiando las orbitas.");
        }

        actualizarMarcadores();

        if (basuraEliminada == basuraTotal) {
            completarNivel();
        }
    }

    private void tocarEstrella(JLabel estrella) {
        puntos = Math.max(0, puntos - 3);
        actualizarMarcadores();

        lblMensaje.setText("Cuidado: las estrellas no son basura. -3 puntos.");
        mostrarVentanaBonita("Cuidado", "Hiciste clic en una estrella.\nLas estrellas no se eliminan.\nSe descontaron 3 puntos.");
    }

    private void usarPista() {
        puntos = Math.max(0, puntos - 3);
        actualizarMarcadores();

        int faltan = basuraTotal - basuraEliminada;
        lblMensaje.setText("Pista usada: -3 puntos.");
        mostrarVentanaBonita("Pista", "Busca los satelites rotos o piezas grises.\nNo hagas clic en las estrellas amarillas.\nBasura restante: " + faltan);
    }

    private void completarNivel() {
        panelOrbitas.setOrbitasBrillantes(true);
        btnsiguiente.setEnabled(true);
        btnPista.setEnabled(false);

        lblMensaje.setText("Excelente: las orbitas vuelven a brillar.");
        mostrarVentanaBonita("Nivel completado", "Limpiaste toda la basura espacial.\nLas orbitas vuelven a brillar.\nPuntaje actual: " + puntos);
    }

    private void actualizarMarcadores() {
        lblPuntaje.setText("Puntos: " + puntos);
    }

