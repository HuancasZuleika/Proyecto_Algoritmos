    private void mostrarVentanaBonita(String titulo, String mensaje) {
        final boolean esAlerta = titulo.toLowerCase().contains("cuidado") || titulo.toLowerCase().contains("falta");
        final Color colorAcento = esAlerta ? new Color(255, 95, 80) : new Color(70, 220, 255);
        final Color colorBrillo = esAlerta ? new Color(255, 230, 85) : new Color(255, 235, 95);

        final JDialog dialogo = new JDialog(this, titulo, true);
        dialogo.setSize(540, 315);
        dialogo.setLocationRelativeTo(this);
        dialogo.setUndecorated(true);

        JPanel panel = new JPanel() {
            protected void paintComponent(Graphics g) {
                super.paintComponent(g);

                Graphics2D g2 = (Graphics2D) g;
                g2.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);

                GradientPaint fondo = new GradientPaint(0, 0, new Color(8, 18, 62), getWidth(), getHeight(), new Color(48, 10, 90));
                g2.setPaint(fondo);
                g2.fillRoundRect(0, 0, getWidth(), getHeight(), 28, 28);

                g2.setColor(colorAcento);
                g2.setStroke(new BasicStroke(4));
                g2.drawRoundRect(3, 3, getWidth() - 7, getHeight() - 7, 28, 28);
            }
        };

        panel.setLayout(null);

        JLabel lblIcono = new JLabel(esAlerta ? "!" : "*", SwingConstants.CENTER);
        lblIcono.setFont(new Font("Arial", Font.BOLD, 48));
        lblIcono.setForeground(colorBrillo);
        lblIcono.setBounds(35, 20, 70, 60);
        panel.add(lblIcono);

        JLabel lblTitulo = new JLabel(titulo, SwingConstants.CENTER);
        lblTitulo.setFont(new Font("Arial", Font.BOLD, 28));
        lblTitulo.setForeground(colorBrillo);
        lblTitulo.setBounds(105, 28, 385, 42);
        panel.add(lblTitulo);

        JTextArea txtMensaje = new JTextArea(mensaje);
        txtMensaje.setFont(new Font("Arial", Font.BOLD, 19));
        txtMensaje.setForeground(Color.WHITE);
        txtMensaje.setBackground(new Color(10, 26, 78));
        txtMensaje.setOpaque(true);
        txtMensaje.setBorder(BorderFactory.createCompoundBorder(
                BorderFactory.createLineBorder(colorAcento, 2),
                BorderFactory.createEmptyBorder(14, 18, 14, 18)));
        txtMensaje.setEditable(false);
        txtMensaje.setFocusable(false);
        txtMensaje.setLineWrap(true);
        txtMensaje.setWrapStyleWord(true);
        txtMensaje.setBounds(50, 100, 440, 96);
        panel.add(txtMensaje);

        JButton btnAceptar = new JButton("Aceptar");
        btnAceptar.setFont(new Font("Arial", Font.BOLD, 17));
        btnAceptar.setForeground(Color.WHITE);
        btnAceptar.setBackground(esAlerta ? new Color(210, 65, 70) : new Color(25, 120, 220));
        btnAceptar.setCursor(new Cursor(Cursor.HAND_CURSOR));
        btnAceptar.setFocusPainted(false);
        btnAceptar.setBorder(BorderFactory.createLineBorder(colorBrillo, 2));
        btnAceptar.setBounds(190, 230, 160, 44);
        btnAceptar.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                dialogo.dispose();
            }
        });

        panel.add(btnAceptar);

        dialogo.setContentPane(panel);
        dialogo.setVisible(true);
    }

    private class PanelOrbitas extends JPanel {

        private boolean orbitasBrillantes = false;

        public void setOrbitasBrillantes(boolean orbitasBrillantes) {
            this.orbitasBrillantes = orbitasBrillantes;
            repaint();
        }

        protected void paintComponent(Graphics g) {
            super.paintComponent(g);

            Graphics2D g2 = (Graphics2D) g;
            g2.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);

            if (!orbitasBrillantes) {
                return;
            }

            g2.setStroke(new BasicStroke(5));
            g2.setColor(new Color(120, 245, 255, 220));

            int centroX = 681;
            int centroY = 380;

            for (int i = 0; i < 6; i++) {
                int ancho = 300 + i * 145;
                int alto = 90 + i * 52;
                g2.drawOval(centroX - ancho / 2, centroY - alto / 2, ancho, alto);
            }

            g2.setColor(new Color(255, 245, 100));
            g2.setFont(new Font("Arial", Font.BOLD, 28));
            g2.drawString("Orbitas limpias y brillantes", 485, 680);
        }
    }


